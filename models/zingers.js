const { Model, DataTypes } = require('sequelize');

const sequelize = require('../config/connection.js');

class zingers extends Model {}

zingers.init(
  {
    id: {
      type: DataTypes.INTEGER,
      allowNull: false,
      primaryKey: true,
      autoIncrement: true
    },
    zinger: {
      type: DataTypes.STRING,
      allowNull: false
    }

  },
  {
    sequelize,
    timestamps: false,
    freezeTableName: true,
    underscored: true,
    modelName: 'zingers',
  }
);

module.exports = zingers;